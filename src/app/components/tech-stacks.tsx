export default function TechStack({
    para,
    id,
}: {
    para?: string;
    id?: string;
}) {
    const [currentSlide, setCurrentSlide] = useState(0);
    const [isAutoPlaying, setIsAutoPlaying] = useState(true);

    // Calculate how many items to show per slide based on screen size
    const getItemsPerSlide = () => {
        if (typeof window !== "undefined") {
            if (window.innerWidth >= 1024) return 8; // lg screens
            if (window.innerWidth >= 768) return 4; // md screens
            return 1; // sm screens
        }
        return 6; // default
    };

    const [itemsPerSlide, setItemsPerSlide] = useState(getItemsPerSlide());

    useEffect(() => {
        const handleResize = () => {
            setItemsPerSlide(getItemsPerSlide());
        };

        window.addEventListener("resize", handleResize);
        return () => window.removeEventListener("resize", handleResize);
    }, []);

    const totalSlides = Math.ceil(technologies.length / itemsPerSlide);

    // Auto-play functionality
    useEffect(() => {
        if (!isAutoPlaying) return;

        const interval = setInterval(() => {
            setCurrentSlide((prev) => (prev + 1) % totalSlides);
        }, 3000);

        return () => clearInterval(interval);
    }, [isAutoPlaying, totalSlides]);

    const goToSlide = (index: number) => {
        setCurrentSlide(index);
    };

    return (
        <div
            id={id}
            className="relative w-full flex flex-col justify-center py-14 md:py-4 px-5"
            onMouseEnter={() => setIsAutoPlaying(false)}
            onMouseLeave={() => setIsAutoPlaying(true)}
        >


            <div className="absolute bottom-[-150px] left-[-400px] -z-50">
                <Image
                    src="/left-hero-bg.svg"
                    alt="Hero Background"
                    width={400}
                    height={400}
                    className="w-[85%] h-full object-cover"
                />
            </div>

            {/* Content */}
            <div className="relative z-10 2xl:max-w-[1740px] mx-auto md:w-[91.5%] 2xl:w-[92.5%]">
                {/* Section Header */}
                <div className="text-center md:mb-16 mb-12 lg:space-y-6">
                    <h2 className="text-[50px] leading-[50px] font-bold tracking-[2px] text-white text-center max-w-[800px] mx-auto uppercase">
                        Technology STACK
                    </h2>
                    <p className="self-stretch text-center justify-start text-white/80 text-[14px] lg:text-xl font-light tracking-[2px] font-futuru leading-[27px]">
                        {para || "Tech That Powers Performance"}
                    </p>
                </div>

                {/* Carousel Container */}
                <div className="relative">
                    {/* Carousel Content */}
                    <div className="overflow-hidden 2xl:mx-12">
                        <div
                            className="flex transition-transform duration-500 ease-in-out"
                            style={{ transform: `translateX(-${currentSlide * 100}%)` }}
                        >
                            {Array.from({ length: totalSlides }).map((_, slideIndex) => (
                                <div key={slideIndex} className="w-full shrink-0">
                                    <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-4 gap-4 md:gap-3">
                                        {technologies
                                            .slice(
                                                slideIndex * itemsPerSlide,
                                                (slideIndex + 1) * itemsPerSlide
                                            )
                                            .map((tech, index) => (
                                                <div key={`${slideIndex}-${index}`} className="">
                                                    <div className="flex items-center gap-4 justify-center 2xl:w-[99%] min-h-[110px] 2xl:min-h-[130px] rounded-[8px] border-t border-b border-e border-[#FFFDFD42] bg-white/10 backdrop-blur-xl transition-all duration-300 transform">
                                                        <div className=" transition-transform duration-300 ">
                                                            {tech.icon}
                                                        </div>
                                                        <span className="text-white tracking-wide font-light font-futuru text-[25px] 2xl:text-[30px] text-center transition-colors duration-300">
                                                            {tech.name}
                                                        </span>
                                                    </div>
                                                </div>
                                            ))}
                                    </div>
                                </div>
                            ))}
                        </div>
                    </div>

                    {/* Dots Navigation */}
                    <div className="flex justify-center mt-8 space-x-2">
                        {Array.from({ length: 3 }).map((_, index) => (
                            <button
                                key={index}
                                onClick={() => goToSlide(index)}
                                className={`w-[6px] h-[6px] rounded-full transition-all duration-300 ${index === currentSlide
                                    ? "bg-green-400 scale-125"
                                    : "bg-gray-600 hover:bg-gray-400"
                                    }`}
                                aria-label={`Go to slide ${index + 1}`}
                            />
                        ))}
                    </div>
                </div>
            </div>
        </div>
    );
}
